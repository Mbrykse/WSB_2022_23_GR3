def kolory(kolor):
    if kolor == "Czerwony":
        return " W tym kolorze mam jablka"
    elif kolor == "Zielony":
        return " W tym kolorze mam papryke"
    elif kolor == "Zolty":
        return " W tym kolorze mam banany"
    else:
        return " Nie mam nic w podanym kolorze"

